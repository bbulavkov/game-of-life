    public void nextGeneration() {
        int[][] future = new int[dimension][dimension];

        for (int x = 0; x < dimension; x++) {
            for (int y = 0; y < dimension; y++) {

                int aliveNeighbours = countAliveNeighbours(x, y);

                int current = universe[x][y];
                future[x][y] = applyRules(current, aliveNeighbours);
            }
        }
        universe = future;
    }


    private int applyRules(int current, int aliveNeighbours) {
        // Cell is lonely and dies
        if ((current == 1) && (aliveNeighbours < 2))
            return 0;

            // Cell dies due to over population
        else if ((current == 1) && (aliveNeighbours > 3))
            return 0;

            // A new cell is born
        else if ((current == 0) && (aliveNeighbours == 3))
            return 1;

            // Remains the same
        else
            return current;
    }

    private int countAliveNeighbours(int x, int y) {
        int aliveNeighbours = 0;

        for (int i = -1; i <= 1; i++) {
            for (int j = -1; j <= 1; j++) {
                if ((x + i >= 0 && x + i < dimension) && (y + j >= 0 && y + j < dimension))
                    aliveNeighbours += universe[x + i][y + j];
            }
        }
        // The cell needs to be subtracted from its neighbours as it was counted before
        aliveNeighbours -= universe[x][y];
        return aliveNeighbours;
    }
